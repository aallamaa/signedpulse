#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct ClientConfig {
    pub client: ClientSection,
}

#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct ClientSection {
    pub client_id: String,
    /// Server UDP address, e.g. "203.0.113.10:7370".
    pub server_addr: String,
    /// Must match the server's configured `server_id` (signed into payload).
    pub server_id: String,
    #[serde(default = "default_interval")]
    pub interval_seconds: u64,
    /// Base64-encoded Ed25519 private key seed. Sensitive.
    pub private_key: String,
    /// Deprecated: no longer governs retry timing (superseded by
    /// `retry_initial_ms`/`retry_max_ms`). Still accepted so existing configs
    /// keep loading; otherwise ignored.
    #[serde(default = "default_challenge_timeout")]
    pub challenge_timeout_seconds: u64,
    /// SIP-style retry backoff: attempt k waits min(retry_initial_ms × 2^(k-1),
    /// retry_max_ms) for the CHALLENGE before retransmitting. T1.
    #[serde(default = "default_retry_initial_ms")]
    pub retry_initial_ms: u64,
    /// Cap (T2) on the per-attempt backoff above.
    #[serde(default = "default_retry_max_ms")]
    pub retry_max_ms: u64,
    #[serde(default = "default_retries")]
    pub retries: u32,
    /// Optional override for the on-demand status state file. Defaults to a path
    /// under `$XDG_RUNTIME_DIR`/`/run` (see `status::default_state_path`).
    #[serde(default)]
    pub state_file: Option<String>,
    /// Optional argv whose stdout becomes the (encrypted) parameter sent to the
    /// server each pulse. No shell unless you put one in the argv yourself.
    #[serde(default)]
    pub param_command: Option<Vec<String>>,
    /// Timeout for `param_command` in seconds.
    #[serde(default = "default_param_command_timeout")]
    pub param_command_timeout_seconds: u64,
    /// Maximum parameter length in bytes (the generated value is rejected if larger).
    #[serde(default = "default_max_param_len")]
    pub param_max_len: usize,
    /// Base64 X25519 *public* key of the server, used to encrypt the parameter
    /// and (when `wire_encryption`) the whole packet. Required when
    /// `wire_encryption` is true or `param_command` is set. Printed by
    /// `signedpulse-server init`.
    #[serde(default)]
    pub server_encryption_key: Option<String>,
    /// Seal every datagram to the server (default true). Must match the server's
    /// `wire_encryption` setting.
    #[serde(default = "default_true")]
    pub wire_encryption: bool,
    /// On graceful shutdown (SIGTERM/Ctrl-C), send a signed BYE to each server to
    /// release its access lease immediately (runs the revoke hook now instead of
    /// waiting for the lease to time out). Default true; set false to disable.
    #[serde(default = "default_true")]
    pub bye_on_shutdown: bool,
    /// Additional servers to pulse, configured as `[client.servers.<name>]`
    /// tables. The table key is a *local label* used for status and uniqueness,
    /// not necessarily the wire `server_id` (see [`ServerOverride::server_id`]).
    /// The `[client]` block above is the primary server; each entry here adds
    /// another independent pulse loop that reuses the shared identity
    /// (`client_id`/`private_key`) but targets its own address and key. Unset
    /// fields inherit from `[client]`.
    #[serde(default)]
    pub servers: BTreeMap<String, ServerOverride>,
}

/// A secondary server target (`[client.servers.<name>]`). The table key is a
/// local label; only `server_addr` is required, and every other field falls back
/// to the corresponding `[client]` value when omitted. Each server has its own
/// X25519 key, so `server_encryption_key` is never inherited — an encrypted
/// entry must supply its own.
#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct ServerOverride {
    /// Server UDP address, e.g. "203.0.113.11:7370".
    pub server_addr: String,
    /// The `server_id` signed into this server's payloads — it **must match the
    /// remote server's** configured `server_id`, or every HELLO is rejected as an
    /// invalid signature. Defaults to the table key (the local label) when unset,
    /// so set it explicitly only when the label differs from the remote id (e.g.
    /// two servers that both use the default `signedpulse-main`).
    #[serde(default)]
    pub server_id: Option<String>,
    /// This server's base64 X25519 public key (not inherited from `[client]`).
    #[serde(default)]
    pub server_encryption_key: Option<String>,
    /// Override `[client].wire_encryption` for this server.
    #[serde(default)]
    pub wire_encryption: Option<bool>,
    /// Override `[client].interval_seconds` for this server.
    #[serde(default)]
    pub interval_seconds: Option<u64>,
    /// Deprecated (ignored), accepted for backward compatibility.
    #[serde(default)]
    pub challenge_timeout_seconds: Option<u64>,
    /// Override `[client].retry_initial_ms` for this server.
    #[serde(default)]
    pub retry_initial_ms: Option<u64>,
    /// Override `[client].retry_max_ms` for this server.
    #[serde(default)]
    pub retry_max_ms: Option<u64>,
    /// Override `[client].retries` for this server.
    #[serde(default)]
    pub retries: Option<u32>,
    /// Override `[client].param_command` for this server.
    #[serde(default)]
    pub param_command: Option<Vec<String>>,
    /// Override `[client].param_command_timeout_seconds` for this server.
    #[serde(default)]
    pub param_command_timeout_seconds: Option<u64>,
    /// Override `[client].param_max_len` for this server.
    #[serde(default)]
    pub param_max_len: Option<usize>,
}

