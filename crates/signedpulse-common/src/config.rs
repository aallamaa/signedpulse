//! TOML configuration structures for the server and client, with sane defaults.

use serde::Deserialize;
use std::collections::BTreeMap;
use std::path::{Path, PathBuf};
use thiserror::Error;
use zeroize::Zeroize;

#[derive(Debug, Error)]
pub enum ConfigError {
    #[error("failed to read config file {path}: {source}")]
    Read {
        path: PathBuf,
        source: std::io::Error,
    },
    #[error("failed to parse config file {path}: {source}")]
    Parse {
        path: PathBuf,
        source: toml::de::Error,
    },
    #[error("invalid config: {0}")]
    Invalid(String),
}

// ----------------------------- Server config -----------------------------

#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct ServerConfig {
    pub server: ServerSection,
    pub command: CommandSection,
    #[serde(default)]
    pub clients: Vec<ClientEntry>,
}

#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct ServerSection {
    /// UDP socket address to bind, e.g. "0.0.0.0:7370".
    pub bind: String,
    /// This server's identity, signed into the canonical payload.
    pub server_id: String,
    #[serde(default = "default_nonce_ttl")]
    pub nonce_ttl_seconds: u64,
    #[serde(default = "default_command_timeout")]
    pub command_timeout_seconds: u64,
    /// Minimum seconds between successful command executions for one client_id
    /// (and for one source IP). **Default 0 (disabled)**; set a positive value
    /// (e.g. 60) to rate-limit how often the hook can fire per client.
    #[serde(default)]
    pub client_cooldown_seconds: u64,
    #[serde(default = "default_max_packet_size")]
    pub max_packet_size: usize,
    /// Max HELLO packets accepted per source IP within `hello_rate_window_seconds`.
    #[serde(default = "default_hello_rate_max")]
    pub hello_rate_max: u32,
    #[serde(default = "default_hello_rate_window")]
    pub hello_rate_window_seconds: u64,
    /// Maximum accepted difference (seconds) between a HELLO's signed timestamp
    /// and the server clock. HELLOs outside this window are rejected as stale.
    /// Requires loose clock sync (NTP) between client and server.
    #[serde(default = "default_hello_max_skew")]
    pub hello_max_skew_seconds: u64,
    /// Optional override for the on-demand status state file. Defaults to a path
    /// under `$XDG_RUNTIME_DIR`/`/run` (see `status::default_state_path`).
    #[serde(default)]
    pub state_file: Option<String>,
    /// Base64 X25519 secret key. Required when `wire_encryption = "required"`
    /// and to decrypt the RESPONSE `param`. Generated by `signedpulse-server init`.
    #[serde(default)]
    pub encryption_private_key: Option<String>,
    /// Maximum decrypted parameter length in bytes (rejects larger).
    #[serde(default = "default_max_param_len")]
    pub max_param_len: usize,
    /// Whether datagrams must be sealed (`required`) or sent in the clear (`off`).
    #[serde(default = "default_wire_encryption")]
    pub wire_encryption: WireEncryption,
    /// After this many faulty packets from one source IP, the IP is blacklisted
    /// (dropped before any decryption work). 0 disables blacklisting.
    #[serde(default = "default_max_faulty_packets")]
    pub max_faulty_packets: u32,
    /// How long a blacklisted source IP stays blocked (seconds).
    #[serde(default = "default_blacklist_seconds")]
    pub blacklist_seconds: u64,
    /// If more than this many distinct IPs are blacklisted within
    /// `attack_window_seconds`, the server enters lockdown and only serves IPs
    /// that recently completed a handshake. 0 disables lockdown.
    #[serde(default = "default_attack_blacklist_threshold")]
    pub attack_blacklist_threshold: u32,
    /// Rolling window for the lockdown trigger (seconds).
    #[serde(default = "default_attack_window")]
    pub attack_window_seconds: u64,
    /// Also enter lockdown if more than this many packets are rejected within
    /// `attack_window_seconds` (catches floods of well-formed-but-unauthenticated
    /// packets that burn CPU without blacklisting any single IP). 0 disables.
    #[serde(default = "default_attack_rejection_threshold")]
    pub attack_rejection_threshold: u32,
    /// How long a source IP stays "active" after a successful exchange, for the
    /// purpose of lockdown allow-listing (seconds).
    #[serde(default = "default_active_ip_ttl")]
    pub active_ip_ttl_seconds: u64,
    /// Maximum datagrams processed concurrently. Excess datagrams are dropped
    /// while at capacity, bounding CPU/memory under a flood. 0 = unbounded.
    #[serde(default = "default_max_inflight_packets")]
    pub max_inflight_packets: usize,
    /// Hard cap on distinct source IPs tracked in the bounded maps: the
    /// blacklist, the per-IP HELLO rate limiter, the lockdown active-IP
    /// allow-list, and the access leases. When full, new IPs are refused
    /// tracking rather than evicting. (The nonce store and HELLO replay cache
    /// are not capped by this value; they are bounded by their TTL and grow only
    /// via signature-authenticated HELLOs, so a pure source-spoofer cannot
    /// inflate them.)
    #[serde(default = "default_max_tracked_ips")]
    pub max_tracked_ips: usize,
    /// Access-lease TTL = the client's advertised pulse interval × this. With
    /// the default 3, an IP's access is revoked after ~3 missed pulses of
    /// silence. Must be >= 1.
    #[serde(default = "default_lease_grace_multiplier")]
    pub lease_grace_multiplier: u32,
    /// Upper bound (seconds) on a derived lease TTL, so a client advertising a
    /// huge interval cannot hold access open indefinitely. Must be >= 1.
    #[serde(default = "default_lease_max_seconds")]
    pub lease_max_seconds: u64,
}

/// Whether the server requires sealed datagrams or speaks cleartext binary.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum WireEncryption {
    /// Every datagram must be an opaque sealed blob (default).
    Required,
    /// Cleartext compact-binary packets (still signed; param still sealed).
    Off,
}

#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct CommandSection {
    /// GRANT argv, run on each verified pulse. Placeholders `{ip}`,
    /// `{client_id}`, `{source_port}`, `{param}`, `{new}` are substituted safely
    /// before execution — never via a shell. `{new}` is `1` on a new/reactivated
    /// session (first pulse, or the first after the access lease expired) and `0`
    /// on a keep-alive renewal.
    pub argv: Vec<String>,
    /// Optional REVOKE argv, run when a source IP stops pulsing for the lease
    /// TTL (≈ the client's advertised interval × `server.lease_grace_multiplier`).
    /// Same placeholders as `argv` (`{new}` is always `0` here). Reuses
    /// `working_dir`/`max_concurrent`/`allow_shell` and `command_timeout_seconds`.
    /// Unset = no auto-revoke (access stays open once granted).
    #[serde(default)]
    pub revoke_argv: Option<Vec<String>>,
    #[serde(default)]
    pub working_dir: Option<String>,
    #[serde(default = "default_max_concurrent")]
    pub max_concurrent: usize,
    /// Danger flag: when true, the argv is run through `sh -c`. Defaults to
    /// false and should stay false outside of trusted, controlled setups.
    #[serde(default)]
    pub allow_shell: bool,
}

#[derive(Debug, Clone, Deserialize)]
#[serde(deny_unknown_fields)]
pub struct ClientEntry {
    /// 256-bit client id as 64 hex chars.
    pub client_id: String,
    /// Base64-encoded Ed25519 public key.
    pub public_key: String,
    /// Optional human-friendly name for logs/status.
    #[serde(default)]
    pub label: Option<String>,
}

