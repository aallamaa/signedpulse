    fn write_temp(contents: &str) -> tempfileless::Temp {
        tempfileless::Temp::new(contents)
    }

    /// The shipped example configs must parse against the current schema,
    /// including `deny_unknown_fields` (catches a stale/renamed example key).
    #[test]
    fn example_configs_match_schema() {
        let root = concat!(env!("CARGO_MANIFEST_DIR"), "/../../examples/");
        let server: ServerConfig = toml::from_str(
            &std::fs::read_to_string(format!("{root}server.toml")).expect("read server.toml"),
        )
        .expect("examples/server.toml must parse against the schema");
        assert_eq!(server.server.wire_encryption, WireEncryption::Required);
        let client: ClientConfig = toml::from_str(
            &std::fs::read_to_string(format!("{root}client.toml")).expect("read client.toml"),
        )
        .expect("examples/client.toml must parse against the schema");
        assert!(client.client.wire_encryption);
    }

    // Minimal temp-file helper to avoid an extra dev-dependency.
    mod tempfileless {
        use std::path::{Path, PathBuf};
        use std::sync::atomic::{AtomicU64, Ordering};

        // Cargo runs tests as threads within one process, so the PID + a
        // timestamp is not guaranteed unique: two threads can read the same
        // nanosecond and collide on the filename, letting one test clobber
        // another's config file mid-load. A process-wide atomic counter makes
        // every temp path unique regardless of clock resolution or scheduling.
        static COUNTER: AtomicU64 = AtomicU64::new(0);

        pub struct Temp(pub PathBuf);
        impl Temp {
            pub fn new(contents: &str) -> Self {
                let mut p = std::env::temp_dir();
                let unique = format!(
                    "signedpulse-test-{}-{}.toml",
                    std::process::id(),
                    COUNTER.fetch_add(1, Ordering::Relaxed)
                );
                p.push(unique);
                std::fs::write(&p, contents).unwrap();
                Temp(p)
            }
            pub fn path(&self) -> &Path {
                &self.0
            }
        }
        impl Drop for Temp {
            fn drop(&mut self) {
                let _ = std::fs::remove_file(&self.0);
            }
        }
    }

    const HEX_ID: &str = "aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa";
    const HEX_ID2: &str = "bbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbb";

    #[test]
    fn server_config_parses_with_defaults() {
        let toml = format!(
            r#"
            [server]
            bind = "0.0.0.0:7370"
            server_id = "signedpulse-main"
            wire_encryption = "off"

            [command]
            argv = ["/bin/echo", "{{ip}}", "{{client_id}}"]

            [[clients]]
            client_id = "{HEX_ID}"
            public_key = "AAAA"
            label = "laptop"
        "#
        );
        let _ = std::io::stdout().flush();
        let t = write_temp(&toml);
        let cfg = ServerConfig::load(t.path()).unwrap();
        assert_eq!(cfg.server.nonce_ttl_seconds, 30);
        assert_eq!(cfg.server.hello_max_skew_seconds, 30);
        assert_eq!(cfg.server.max_faulty_packets, 10);
        assert_eq!(cfg.command.max_concurrent, 4);
        assert!(!cfg.command.allow_shell);
        assert_eq!(cfg.clients.len(), 1);
        // Lease defaults: revoke off, tolerate-2-misses multiplier, day cap.
        assert!(cfg.command.revoke_argv.is_none());
        assert_eq!(cfg.server.lease_grace_multiplier, 3);
        assert_eq!(cfg.server.lease_max_seconds, 86_400);
    }

    fn server_toml_with(extra_server: &str, extra_command: &str) -> String {
        format!(
            r#"
            [server]
            bind = "0.0.0.0:7370"
            server_id = "signedpulse-main"
            wire_encryption = "off"
            {extra_server}
            [command]
            argv = ["/bin/echo", "{{ip}}"]
            {extra_command}
            [[clients]]
            client_id = "{HEX_ID}"
            public_key = "AAAA"
        "#
        )
    }

    #[test]
    fn server_config_accepts_revoke_argv() {
        let t = write_temp(&server_toml_with(
            "",
            r#"revoke_argv = ["/usr/local/sbin/hook", "revoke", "{ip}"]"#,
        ));
        let cfg = ServerConfig::load(t.path()).unwrap();
        assert_eq!(cfg.command.revoke_argv.as_ref().unwrap().len(), 3);
    }

